let getAverageAge = (arr) =>{
    let ageSum = arr.reduce((sum, current) => (sum + current.age), 0 ) //usa o reduce para somar as idades
    let avgAge = ageSum / arr.length //faz a m√©dia das idades
    return avgAge
}

let john = { name: "John", age: 25 };
let pete = { name: "Pete", age: 30 };
let mary = { name: "Mary", age: 29 };

